
// basic data type
// check the documentation for the compiler
typedef char BYTE;                      /* 1 byte */
typedef long INT32;                     /* 4 bytes */
typedef unsigned long UINT32;           /* 4 bytes */
typedef __int64 INT64;                  /* 8 bytes */
typedef unsigned __int64 UINT64;        /* 8 bytes */
typedef double DOUBLE;                  /* 8 bytes */

typedef HANDLE FHANDLE;                         /* file handle */
typedef HANDLE MHANDLE;                         /* module handle */
typedef HANDLE THANDLE;                         /* thread handle */
